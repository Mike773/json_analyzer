"""Двухстадийный агент-аналитик.

Стадия 1 (сбор) — `create_agent` из LangChain 1.x вызывает инструменты в цикле.
У GigaChat запрос С ФУНКЦИЯМИ ограничен ~4096 токенами, поэтому выдача инструментов
держится компактной.

Стадия 2 (синтез) — отдельный вызов чат-модели БЕЗ инструментов получает вопрос и
все собранные данные обычным текстом. Без функций доступен полный контекст модели
(у GigaChat-2-Max — 128k), поэтому финальный ответ можно собрать из всех данных
сразу и без шума tool-схем.
"""
from __future__ import annotations

import json
from typing import Any

from langchain.agents import create_agent
from langchain_core.messages import HumanMessage, SystemMessage, ToolMessage
from langgraph.errors import GraphRecursionError

from analyzer.llm import build_chat_model

# Цикл сбора (стадия 1) тащит на каждом шаге всю историю вызовов инструментов.
# Чтобы контекст не перерос окно модели, выдача каждого инструмента усекается,
# а число шагов цикла ограничено.
_TOOL_OUTPUT_CAP = 6000
_RECURSION_LIMIT = 50

# Идентичные повторные вызовы инструментов в одном цикле сбора — типичный
# признак зацикливания модели; такой вызов не выполняется заново. Множество
# сбрасывается в начале каждого run_gather.
_SEEN_CALLS: set[str] = set()

# Мягкий потолок числа фактически выполненных вызовов инструментов за один сбор.
# Достигнув его, агент на любой следующий вызов получает подсказку завершиться —
# это переводит «упор в жёсткий лимит рекурсии» в управляемую раннюю остановку.
# Перебор-энумерация (find_flags/describe_metric/get_metric по каждой метрике) —
# хвост сбора; широкие вызовы агент делает первыми, так что нужные данные к
# моменту срабатывания бюджета уже собраны. Счётчик сбрасывается в run_gather.
_TOOL_CALL_BUDGET = 18
_CALL_COUNT: list[int] = [0]

# Ответы-заглушки на повторный вызов и на исчерпание бюджета вызовов, плюс порог,
# после которого цикл сбора принудительно завершается: модель зациклилась либо
# исчерпала бюджет и не реагирует на заглушку.
_REPEAT_NOTICE = (
    "Этот инструмент уже вызывался с такими же аргументами — его результат "
    "есть выше. НЕ повторяй вызов: используй уже собранные данные или заверши "
    "сбор."
)
_BUDGET_NOTICE = (
    "Достигнут предел числа обращений к инструментам — собранных данных "
    "достаточно. НЕ вызывай больше инструменты: заверши сбор и дай ответ по уже "
    "полученным данным."
)
_MAX_REPEAT_BLOCKS = 3

